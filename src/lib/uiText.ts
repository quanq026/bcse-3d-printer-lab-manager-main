export type TranslationKey = keyof typeof translations.VN;
export type UiText = typeof uiText.VN;
export type UiLang = Lang;

export function getUiText(lang: Lang) {
  return uiText[lang] ?? uiText.VN;
}

export function fillText(template: string, params?: Record<string, string | number>) {
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (_, key) => String(params[key] ?? ''));
}

